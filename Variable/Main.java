package day03;

public class Main {

	public static void main(String[] args) {

		 // 숫자와 문자 
		//  컴퓨터가 결국엔 계산기 기계야 
	 // 이 계산기로써의 컴퓨터 역할을 쓰고 
		// 기호와 데이터를 어떻게 쓰는가. 
		
		// 데이터 형식이 많이 있는데 차차 알게 되면되고 
		
		// 가장 익숙한 data type은 숫자와 문자.
		// 한글 = 자료형 = data type  = 데이터형 이라고 한다.
		
		System.out.println(1+2);
		// 결과 : 3 
		System.out.println(1.2+1.3);
		// 결과 : 2.5 
		// 곱하기 * 
		System.out.println(2*5);
		System.out.println(2/5);
		
		// 숫자  int 
		// 문자 와 문자 열  
//		문자 : 자료형 : Character  char String
//		문자열 : 자료형 : String    구분
		System.out.println('A');
		// c 언어 : const  
		// 자바 : final 
		final double pi = 3.14; // 상수 
		
		System.out.println("앙 개꿀\n"+pi+"하하");
//		Variable
		
	 
		
	}

}
