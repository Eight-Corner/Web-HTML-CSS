package day03;

public class Operator {

	public static void main(String[] args) {
		
//		+(덧) -(빼) *(곱) / (나누기) % (나머지)
		// 산술 연산자 
		
		// result 의 값 result - 결과
		int result = 1 + 2 ;
		System.out.println(result);
//		->
		result = result - 1;
		System.out.println(result);
//		->
		result = result * 2;
		System.out.println(result);
		
		result = result / 2 ;
		System.out.println(result);
		
		result = result % 2 ; // 나머지 . 
		System.out.println(result);
		
		int result1 = 3 * 11;// == 33;
		 result1 = result1 % 2; // 
		System.out.println(result1);
		
		result = result + 8 ;
		System.out.println(result);
		
		result = result % 7;  //
		System.out.println(result);
		
		
	}

}
